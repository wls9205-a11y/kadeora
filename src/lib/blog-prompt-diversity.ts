function pick<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

/**
 * AI 프롬프트에 스타일 다양화 지시를 추가
 * @param basePrompt 기본 프롬프트
 * @returns 스타일 변형이 추가된 프롬프트
 */
export function diversifyPrompt(basePrompt: string): string {
  const style = pick(OPENING_STYLES);
  const structure = pick(STRUCTURE_VARIANTS);
  const closing = pick(CLOSING_STYLES);
  const emoji = pick(EMOJI_STYLES);
  
  return `${basePrompt}

[문체 지시]
- ${style}
- ${structure}
- ${closing}
- ${emoji}
- 도입부는 매번 다른 방식으로 시작하세요. "~를 분석했습니다" 같은 정형화된 문장은 피하세요.
- 소제목(h2)의 순서와 표현을 매번 다르게 해주세요.`;
}

