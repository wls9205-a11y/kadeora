export function ensureMinLength(content: string, category: string, minChars: number = 1500): string {
  if (content.length >= minChars) return content;
  const pad = PADDING[category] || PADDING.general;
  const lastHr = content.lastIndexOf('\n---\n');
  if (lastHr > 0) {
    return content.slice(0, lastHr) + pad + content.slice(lastHr);
  }
  return content + pad;
}
