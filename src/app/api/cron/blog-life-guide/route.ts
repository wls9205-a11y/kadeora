export const GET = withCronAuth(async (req: NextRequest) => {
  const url = new URL(req.url);
  const offset = parseInt(url.searchParams.get('offset') || '0', 10);
  const limit = parseInt(url.searchParams.get('limit') || '5', 10);

  const targets = GUIDES.slice(offset, offset + limit);

  if (targets.length === 0) {
    return NextResponse.json({ ok: true, message: 'No guides in range', offset, limit });
  }

  const admin = getSupabaseAdmin();
  const today = new Date().toISOString().slice(0, 10);
  const results: { slug: string; ok: boolean; reason?: string }[] = [];

  for (const guide of targets) {
    const rawContent = guide.content;
    const content = ensureMinLength(rawContent, guide.category, 1500);

    const result = await safeBlogInsert(admin, {
      slug: guide.slug,
      title: guide.title,
      content,
      excerpt: guide.title,
      category: guide.category,
      tags: guide.tags,
      source_type: 'auto',
      cron_type: 'blog-life-guide',
      data_date: today,
      image_alt: generateImageAlt(guide.category, guide.title),
      meta_description: generateMetaDesc(content),
      meta_keywords: generateMetaKeywords(guide.category, guide.tags),
      is_published: true,
    });

    results.push({
      slug: guide.slug,
      ok: result.success,
      reason: result.reason,
    });
  }

  const created = results.filter((r) => r.ok).length;
  const skipped = results.filter((r) => !r.ok).length;

  return NextResponse.json({
    ok: true,
    total: GUIDES.length,
    offset,
    limit,
    created,
    skipped,
    results,
  });
});
